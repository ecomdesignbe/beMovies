// Swiper
var swiper = new Swiper(".mySwiper", {
  slidesPerView: 4,
  spaceBetween: 19,
  
  navigation: {
    nextEl: '.search-right',
    prevEl: '.search-left',
  },

  breakpoints: {
    // when window width is >= 100px
    100: {
      slidesPerView: 1,
      spaceBetween: 19
    },
    // when window width is >= 320px
    320: {
      slidesPerView: 1,
      spaceBetween: 19
    },
    // when window width is >= 480px
    480: {
      slidesPerView: 2,
      spaceBetween: 19
    },
    // when window width is >= 640px
    640: {
      slidesPerView: 3,
      spaceBetween: 19
    },    
    // when window width is >= 940px
    940: {
      slidesPerView: 4,
      spaceBetween: 19
    }
  }
})

